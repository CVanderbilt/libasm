			global	_ft_read

			section	.text

_ft_read:
            xor     rax, rax
            sub     rax, 1
            cmp     edx, 0
            jl      end
            ;cmp     esi, 0
            ;jl      end
            mov     rax, 0x2000003
            syscall
end:
            ret
